    private void tb1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb1MouseClicked

        int index = tb1.getSelectedRow();
        TableModel model = tb1.getModel();
        txtId.setText(String.valueOf(model.getValueAt(index,0).toString()));
        txtPassword.setText(model.getValueAt(index,1).toString());
        String per = model.getValueAt(index, 2).toString();
        if(per.equals("Admin"))
        cbaAdmin.setSelected(true);
        else cbUser.setSelected(true);
    }//GEN-LAST:event_tb1MouseClicked


