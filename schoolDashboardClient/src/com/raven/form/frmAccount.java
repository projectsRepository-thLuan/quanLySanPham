    public void bingdingTable( List<User> listData){
        DefaultTableModel tblModel = (DefaultTableModel)tb1.getModel();
        while(tblModel.getRowCount() > 0)
        {
            tblModel.removeRow(0);
        }
        if (listData != null) {
            for (User User : listData) {
                String per;
                if(User.getPer()==0)
                    per = "Admin";
                else per = "User";
                String tbData[] = { User.getUsername(), User.getPassword() , per};
                tblModel.addRow(tbData);
            }
        }
    }

