    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        sendToServer sts = new sendToServer();
        String username = txtId.getText();
        String pass = txtPassword.getText();
        String per;
        if(cbaAdmin.isSelected())
            per ="0";
        else per = "1";
        String request = "Create-Account-"+username+"-"+pass+"-"+per;
        List<User> listData = (List<User>) sts.Action(request);
        if(listData != null){
            JOptionPane.showMessageDialog(null, "Đã thêm thành công");
        }
        else{
            JOptionPane.showMessageDialog(null, "Thêm không được");
        }
        bingdingTable(listData);
    }//GEN-LAST:event_btnCreateActionPerformed

